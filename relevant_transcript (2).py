"""Index transcript into Pinecone vector DB"""

print("Indexing transcript segments...")
for i, chunk in enumerate(sample_transcript):
    vector = embedding_model.encode(chunk["text"]).tolist()
    index.upsert(vectors=[(
        f"chunk_{i}",
        vector,
        {"text": chunk["text"], "start": chunk["timestamp_start"], "end": chunk["timestamp_end"]}
    )])
print("Indexing complete!")

"""Search the lecture transcript vector DB for blocks matching query summary"""

def search_transcript(query_summary: str) -> str:
    query_vector = embedding_model.encode(query_summary).tolist()
    results = index.query(vector=query_vector, top_k=2, include_metadata=True)

    context_blocks = []
    for match in results['matches']:
        meta = match['metadata']
        context_blocks.append(f"[{meta['start']} - {meta['end']}]: {meta['text']}")

    return "\n\n".join(context_blocks)

#wrap in ADK function tool
transcript_search_tool = FunctionTool(
    func=search_transcript
)

llm_provider = LiteLlm(model="groq/llama-3.3-70b-versatile")

system_instruction = """
You are an AI Teaching Assistant agent. Your job is to answer a student's question based strictly on the retrieved lecture transcript chunks provided by your search tool.
For every response, you must follow this exact Markdown format:

### **Summary Answer**
[Provide a clear, direct, and concise answer to the student's problem using the transcript data.]

### **Source Timestamp**
[Provide the exact timestamp range(s) where the answer was explicitly found, e.g., `[00:30] - [01:10]`]
"""

agent = LlmAgent(
    name="teaching_assistant_agent",
    model=llm_provider,
    tools=[transcript_search_tool]
)

# agent.llm = llm_provider # Removed: LlmAgent object has no field 'llm'
