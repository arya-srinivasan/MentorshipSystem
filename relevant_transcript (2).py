session_service = InMemorySessionService()
session = Session(
    id=SESSION_ID,
    app_name=APP_NAME,
    user_id=USER_ID
)

runner = Runner(session_service=session_service, app_name=APP_NAME, agent=agent)
student_query = "Student is having trouble understanding what a view is in SQL and specifically if a view changes when a table changes."

full_prompt = f"""
System Instructions:
{system_instruction}

User Query:
{student_query}
"""

formatted_message = types.Content(
    role="user",
    parts=[types.Part.from_text(text=full_prompt)]
)

async def main():
    # Register the session
    active_session = await session_service.create_session(
        app_name=APP_NAME,
        user_id=USER_ID
    )

    print(f"Orchestrating ADK Agent execution step for session: {active_session.id}...")

    # Pass our formatted structured message object into the runner generator
    run_generator = runner.run(
        user_id=USER_ID,
        session_id=active_session.id,
        new_message=formatted_message
    )

    final_text = ""
    for step in run_generator:
        # Check if the step has the 'content' attribute from the GenAI model response
        if hasattr(step, 'content') and step.content and step.content.parts:
            # Join text from all parts if there are multiple, or just grab the first
            final_text = "".join([part.text for part in step.content.parts if hasattr(part, 'text')])
        # Fallbacks for other ADK event structures
        elif hasattr(step, 'response') and hasattr(step.response, 'text'):
            final_text = step.response.text
        elif hasattr(step, 'text'):
            final_text = step.text

    print("\n" + "="*20 + " AGENT OUTPUT " + "="*20)
    if final_text:
        print(final_text)
    else:
        print("Raw Last Step:", step)

# Run it in your Colab cell
await main()